<h2>Megan Knight - University of Hertfordshire</h2>


<p>I am a researcher at the University of Hertfordshire, and I am running a project investigating how social media influences the news we consume, and what we know about the world. 
<p>In order to do this, I am looking for people to contribute anonymised data from their own Facebook profile, which I can then analyse. I need to see individual people’s news feeds because Facebook does not show the same information to everyone, but only a selection of news and posts based on who you are, who your friends are, and the kinds of stories you have liked or shared yourself. 
<p>If you agree to this project, you will be asked to install an application on your Facebook account, which will extract the most recent posts from your timeline, and pages you have liked, remove all identifying information (your name, and the names of all of your friends will be replaced by random strings of numbers, and I will never know who you are, or whether you participated) and send me the contents of that. It will also send me your age, gender and political views from Facebook’s “About” page. 
<p>The application will also ask you five questions about your overall level of interest in news and current affairs.  The whole process will take no more than five minutes and you will be able to withdraw at any stage. 

<p>The data that is extracted will be sent to me electronically, through a secure server. I will then analyse the data I receive and plan to publish my research. To be clear – no names, neither yours or those of your friends, will be recorded as part of this study. I am only looking at the news sources you see and, where you have included it in your profile, information about your age, gender and political outlook. No personally identifying information at all will be collected.

<p>You can see <a href= http://www.meganknight.uk/uncategorized/facebook-and-the-news/ target=_new>more information about why I am doing this, and the justifications for its importance on my personal blog</a>, and can <a href=http://researchprofiles.herts.ac.uk/portal/en/persons/megan-knight(8150ae0b-0ac4-49de-8013-1262707f28f8).html  target =_new>read about my previous research at Academia.edu</a>
