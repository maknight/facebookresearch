<h5>Megan Knight - University of Hertfordshire</h5>

<p>I am a researcher at the University of Hertfordshire, and I am running a project investigating how social media influences the news we consume, and what we know about the world.

<p>In order to do this, I am looking for people to contribute anonymised data from their own Facebook profile, which I can then analyse. I need to see individual people’s news feeds because Facebook does not show the same information to everyone, but only a selection of news and posts based on who you are, who your friends are, and the kinds of stories you have liked or shared yourself.

<p>If you agree to this project, you will be asked to install an application on your Facebook account, which will extract the most recent posts from your news feed and the list of pages you have liked, remove all identifying information (your name, and the names of all of your friends will be replaced by random strings of numbers, and I will never know who you are, or whether you participated) and send me only the content of your news feed and the pages you have liked. It will also send me your age, gender and political views from Facebook’s “About” page.

<p>The application will also ask you five questions about your overall level of interest in news and current affairs.  The whole process will take no more than five minutes and you will be able to withdraw at any stage.

<p>The data that is extracted will be sent to me electronically, through a secure server. I will then analyse the data I receive and plan to publish my research. To be clear – no names, neither yours or those of your friends, will be recorded as part of this study. I am only looking at the news sources you see and, where you have included it in your profile, information about your age, gender and political outlook. No personally identifying information at all will be collected.

<p>If you want to participate, please click this link

<p>This project is an operationalisation of the ideas expressed in the paper "Facebook is changing news", to be published in Rhodes Journalism Review, 2017 (a draft is available at http://www.meganknight.uk/uncategorized/facebook-and-the-news/). The research will analyse the news content of people's Facebook feeds in order to determine the impact false and overly biased news sources have on people's understanding of the world. The initial stages of the study will simply focus on which news stories are seen by users, and whether these correspond with dominant views on current affairs as presented by a selection of mainstream UK news outlets.

<p>This research will employ the Facebook public API to access individual users’ Facebook feeds (with permission, and fully anonymised) in order to determine the nature and quantity of news material presented within this platform. The data will be extracted by means of a Facebook App, to which users will agree. The data is then anonymised and sent to the research repository.

<p>Analysis will be by content analysis, and comparisons will be made with news accessed through more traditional means, and through comparisons with basic demographic information as gathered by the Facebook app.

